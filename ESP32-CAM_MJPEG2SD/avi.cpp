/* AVI 文件格式：
文件头：
 310 字节
每个 jpeg：
 4 字节 00dc 标记
 4 字节 jpeg 大小
 jpeg 帧内容
 0-3 字节填充以对齐 DWORD 边界
每个 PCM（音频文件）：
 4 字节 01wb 标记
 4 字节 pcm 大小
 pcm 内容
 0-3 字节填充以对齐 DWORD 边界
文件尾：
 4 字节 idx1 标记
 4 字节索引大小
 每个 jpeg：
  4 字节 00dc 标记
  4 字节 0000
  4 字节 jpeg 位置
  4 字节 jpeg 大小
 每个 pcm：
  4 字节 01wb 标记
  4 字节 0000
  4 字节 pcm 位置
  4 字节 pcm 大小
*/

